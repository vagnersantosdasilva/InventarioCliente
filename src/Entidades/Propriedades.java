package Entidades;

public class Propriedades 
{
	private String servidor;
	private String porta;
	private String tempoDeAtualizacao;
	private String programFiles;
	private String programFilesx86;
	private String winDir;
	private String winUpdate;
	private String setupAct;
	
	public String getServidor() {
		return servidor;
	}
	public void setServidor(String servidor) {
		this.servidor = servidor;
	}
	public String getPorta() {
		return porta;
	}
	public void setPorta(String porta) {
		this.porta = porta;
	}
	public String getTempoDeAtualizacao() {
		return tempoDeAtualizacao;
	}
	public void setTempoDeAtualizacao(String tempoDeAtualizacao) {
		this.tempoDeAtualizacao = tempoDeAtualizacao;
	}
	public String getProgramFiles() {
		return programFiles;
	}
	public void setProgramFiles(String programFiles) {
		this.programFiles = programFiles;
	}
	public String getProgramFilesx86() {
		return programFilesx86;
	}
	public void setProgramFilesx86(String programFilesx86) {
		this.programFilesx86 = programFilesx86;
	}
	public String getWinDir() {
		return winDir;
	}
	public void setWinDir(String winDir) {
		this.winDir = winDir;
	}
	public String getWinUpdate() {
		return winUpdate;
	}
	public void setWinUpdate(String winUpdate) {
		this.winUpdate = winUpdate;
	}
	public String getSetupAct() {
		return setupAct;
	}
	public void setSetupAct(String setupAct) {
		this.setupAct = setupAct;
	}
	
}
